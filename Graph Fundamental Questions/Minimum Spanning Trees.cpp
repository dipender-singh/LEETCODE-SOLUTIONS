//And every Node should be reachable by every other Node in the MST.
//Now for a Graph we can create various different Tree's and out of all the Spanning Tree's generated the Spanning Tree which has the lowest sum of all the weight of the 
//edges that particular Spanning Tree will be the Minimum Spanning Tree

